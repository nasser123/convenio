    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        this.convenio.setNome(jTextFieldNome.getText());
        this.convenio.setResponsavel(jTextFieldResponsavel.getText());
        this.convenio.setTelefone(jTextFieldTelefone.getText());
        this.convenio.setNrparcelas(jComboBoxNrParcelas.getSelectedIndex() + 1);
        this.convenio.setVencimento(jComboBoxVencimento.getSelectedIndex() + 1);
        
        try{
            this.convenio.setComissao(Double.parseDouble(jFormattedTextField1.getText()));
        }catch(Exception e){
            this.convenio.setComissao(Double.valueOf(5.0));
        }
        
        
        int teste = jComboBoxDataLimite.getSelectedIndex();
        if(teste < 28){
        this.convenio.setDatalimite(jComboBoxDataLimite.getSelectedIndex() + 1);
        }else{
            this.convenio.setDatalimite(0);
        }
        

        if (jComboBoxSituacao.getSelectedIndex() == 0) {
            this.convenio.setSituacao("ativo");
        }
        if (jComboBoxSituacao.getSelectedIndex() == 1) {
            this.convenio.setSituacao("inativo");
        }
        if (jComboBoxSituacao.getSelectedIndex() == 2) {
            this.convenio.setSituacao("bloqueado");
        }

        ConvenioController cc = new ConvenioController();
        boolean gravou;
        try {
            if (this.convenio.getIdconvenio() != null) {
                gravou = cc.alterar(this.convenio, true);

            } else {
                gravou = cc.inserir(this.convenio);
            }

            if (gravou) {
                new TelaListaConvenio().setVisible(true);
                this.dispose();
            }
        } catch (SQLException ex) {
            ConfigurationFactory.getLOG().warn(ex.getMessage());
        }

    }//GEN-LAST:event_jButtonGravarActionPerformed

